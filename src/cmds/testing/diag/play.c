static float32_t arm_sin_f32(
  float32_t x)
{
  float32_t sinVal, fract, in;                           /* Temporary variables for input, output */
  uint16_t index;                                        /* Index variable */
  float32_t a, b;                                        /* Two nearest output values */
  int32_t n;
  float32_t findex;

  /* Special case for small negative inputs */
  if ((x < 0.0f) && (x >= -1.9e-7f)) {
     return x;
  }

  /* input x is in radians */
  /* Scale the input to [0 1] range from [0 2*PI] , divide input by 2*pi */
  in = x * 0.159154943092f;

  /* Calculation of floor value of input */
  n = (int32_t) in;

  /* Make negative values towards -infinity */
  if (x < 0.0f)
  {
    n--;
  }

  /* Map input value to [0 1] */
  in = in - (float32_t) n;

  /* Calculation of index of the table */
  findex = (float32_t) FAST_MATH_TABLE_SIZE * in;

  index = ((uint16_t)findex) & 0x1ff;

  /* fractional value calculation */
  fract = findex - (float32_t) index;

  /* Read two nearest values of input value from the sin table */
  a = sinTable_f32[index];
  b = sinTable_f32[index+1];

  /* Linear interpolation process */
  sinVal = (1.0f-fract)*a + fract*b;

  /* Return the output value */
  return (sinVal);
}

static double _sin(double x) {
	arm_sin_f32(x);
	return x;
}

static double _sin_w = 100.;
static int _sin_h = 10000;
static double phase = 0.0;
static int sin_callback(const void *inputBuffer, void *outputBuffer,
    unsigned long framesPerBuffer,
    const PaStreamCallbackTimeInfo* timeInfo,
    PaStreamCallbackFlags statusFlags,
    void *userData) {
  uint16_t *data;

  data = outputBuffer;

  for (int i = 0; i < framesPerBuffer; i++) {
    double x = phase;
    phase += 2 * 3.14159265359 / _sin_w;
    if (phase >= 2 * 3.14159265359) {
      phase -= 2 * 3.14159265359;
    }
		*data++ = (uint16_t) ((1. + _sin(x)) * _sin_h); /* Left channel  */
		*data++ = (uint16_t) ((1. + _sin(x)) * _sin_h); /* Right channel */
	}

	return 0;
}

int play_setVolume(int vol)
{
	if (vol < 0 || vol > 100) {
		printf("Volume must be between 0 and 100\n");
		return -1;
	}

	extern uint8_t BSP_AUDIO_OUT_SetVolume(uint8_t Volume);
	BSP_AUDIO_OUT_SetVolume(vol);
	printf("Volume set to %d\n", vol);

	return 0;
}

